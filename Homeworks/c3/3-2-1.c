#include <stdio.h>
int main(void)
{
    int a, b, t;
    /**************found************/
    a = 1; // 此处填写通过赋值语句给变量 a 赋值的语句;
    /**************found************/
    b = 2; // 此处填写通过赋值语句给变量 b 赋值的语句;
    t = a;
    a = b;
    b = t; /* 以上三条赋值语句实现 a,b 变量值的交换 */
    /**************found************/
    printf("a=%d, b=%d\n", a, b); // 此处填写输出变量 a、b 的值的语句;
    return 0;
}
